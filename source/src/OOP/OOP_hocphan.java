package OOP;


public class OOP_hocphan {
	String mahocphan;
	String tenhocphan;
	int sodvht;
	String manganh;
	String hocki;
	int tienhocphan;

	public OOP_hocphan(String mahocphan, String tenhocphan, int sodvht, String manganh, String hocki, int tienhocphan) {
		this.mahocphan = mahocphan;
		this.tenhocphan = tenhocphan;
		this.sodvht = sodvht;
		this.manganh = manganh;
		this.hocki = hocki;
		this.tienhocphan = tienhocphan;
	}
	public String getMahocphan() {
		return mahocphan;
	}
	public void setMahocphan(String mahocphan) {
		this.mahocphan = mahocphan;
	}
	public String getTenhocphan() {
		return tenhocphan;
	}
	public void setTenhocphan(String tenhocphan) {
		this.tenhocphan = tenhocphan;
	}
	public int getSodvht() {
		return sodvht;
	}
	public void setSodvht(int sodvht) {
		this.sodvht = sodvht;
	}
	public String getManganh() {
		return manganh;
	}
	public void setManganh(String manganh) {
		this.manganh = manganh;
	}
	public String getHocki() {
		return hocki;
	}
	public void setHocki(String hocki) {
		this.hocki = hocki;
	}
	public int getTienhocphan() {
		return tienhocphan;
	}
	public void setTienhocphan(int tienhocphan) {
		this.tienhocphan = tienhocphan;
	}
}